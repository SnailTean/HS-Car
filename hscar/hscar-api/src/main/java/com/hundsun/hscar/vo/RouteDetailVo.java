package com.hundsun.hscar.vo;

public class RouteDetailVo {
	private String departure;
	private String destination;
	private String dep_coordinate;
	private String des_coordinate;
	
	public String getDeparture() {
		return departure;
	}
	public void setDeparture(String departure) {
		this.departure = departure;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getDep_coordinate() {
		return dep_coordinate;
	}
	public void setDep_coordinate(String dep_coordinate) {
		this.dep_coordinate = dep_coordinate;
	}
	public String getDes_coordinate() {
		return des_coordinate;
	}
	public void setDes_coordinate(String des_coordinate) {
		this.des_coordinate = des_coordinate;
	}
}
